<h1>I don't know</h1>

<p>
  thinking about the future makes me anxious. There are too many unknown and not
  enough guarantees. As a christian, I should know better, but that is it I do
  not know any better.
</p>

<p>
  I would like to be able to secure the future of my family. Be able to provide
  in full but never in excess. Be able to maintain my dignity, however, I am
  certain that I do not understand pride.
</p>

<p>
  Seeing how others are more accomplished, not lacking in wealth, and how their
  plans are coming to fruition, not knowing why am i not that could lead me to
  become bitter. But somehow I am comforted by not knowing.
</p>

<p>
  Father Gregory Pine in his talk with Shayne Smith said that "when we get bump,
  the thing the slushes those are the contents" he futher added that if we fill
  ourselves with good things, such as gratitude to God, when we get bumped, that
  is what's going spill over the edge. Its an easy concept but only by God's
  grace that can ever be achieved.
</p>

<p>
  Me not knowing is me being bumped. Me being able to accept what I lack is
  God's grace. Instead of me being bitter, I will celebrate their accomplishment
  and hopefully learn from them. Life is hard, but it can be harder If going to
  be bitter.
</p>
