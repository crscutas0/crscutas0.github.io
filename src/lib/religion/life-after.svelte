  <p>
    It is quite common for people to claim that the person who passed is
    probably in heaven now. Or they are now an angel watching over us. This
    claims are interesting in a sense that it exposes our hope. But I have seen
    this claim being questioned and I want to give an explanation.
  </p>

  <h2>Are they in heaven now?</h2>

  <p> The answer is "we don't know". </p>

  <p>
    My assumption is that these claims are uttered as words of consolation. We
    wish for their suffering to end and they may find rest with God. And because
    we don't know, the best that we could do is to pray for their soul. Lucky
    for us Filipinos a series of novena's and mass is offered to the deceased to
    help them in their journey.
  </p>

  <p>
    We Catholics have declared people to be in heaven, and we call them saints.
    There is a process on how the Catholic Church declares a saint, which we
    will not be getting into now.
  </p>

  <h2>Are they an angel?</h2>

  <p>
    It is common to hear people say that the deceased has “become an angel.”
    While this may sound comforting, it is not what we believe as Catholics.
  </p>

  <p> Humans do not become angels. </p>

  <p>
    Angels and human beings are both created by God, but they are fundamentally
    different. Angels are pure spirit—they do not have bodies. Human beings, on
    the other hand, are created as a union of body and soul. This is not just a
    temporary condition; it is part of what we are by nature.
  </p>

  <p>
    To say that a human becomes an angel after death would mean becoming
    something entirely different from what God created us to be. But death does
    not change our nature—it only separates the soul from the body for a time.
  </p>

  <p>
    This is why the resurrection is so important in Christian belief. We do not
    hope to remain as disembodied spirits forever. We believe that, in the end,
    God will restore us fully—body and soul. Even in the resurrection of Jesus
    Christ, he did not abandon his body. He rose with it, showing us what awaits
    those who are saved.
  </p>

  <p>
    So while it may be comforting to imagine our loved ones as angels watching
    over us, our hope is actually greater than that. We are not meant to become
    angels—we are meant to be fully human, restored and perfected in the
    presence of God.
  </p>
</div>
